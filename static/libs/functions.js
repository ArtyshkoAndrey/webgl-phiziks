class WikiAtoms {
  constructor () {
    this.atoms = {}
  }

  add (name, symbol, color, radius, covalentRadius, doubleCovalentRadius, tripleCovalentRadius) {
    this.atoms[symbol] = new WikiAtom(name, symbol, color, radius, covalentRadius, doubleCovalentRadius, tripleCovalentRadius)
  }

  get (symbol) {
    return this.atoms[symbol]
  }

  parseJSON (json) {
    this.atoms = JSON.parse(json)
  }

  toJSON () {
    return JSON.stringify(this.atoms)
  }
}

